// Get user's coding statistics
export async function getUserCodingStats() {
  try {
    const { userId } = auth();
    if (!userId) {
      // Return default stats if user not authenticated
      return {
        totalSubmissions: 0,
        totalAccepted: 0,
        totalAttempted: 0,
        acceptanceRate: 0,
        languageDistribution: {},
        recentSubmissions: [],
      };
    }

    const user = await db.user.findUnique({
      where: { clerkUserId: userId },
    });

    if (!user) {
      // Return default stats if user not found
      return {
        totalSubmissions: 0,
        totalAccepted: 0,
        totalAttempted: 0,
        acceptanceRate: 0,
        languageDistribution: {},
        recentSubmissions: [],
      };
    }

    const submissions = await db.codeSubmission.findMany({
      where: { userId: user.id },
    });

    const accepted = submissions.filter(
      (s) => s.verdict === "accepted"
    ).length;
    const attempted = new Set(submissions.map((s) => s.challengeId)).size;
    const totalSubmissions = submissions.length;

    // Language distribution
    const languageDistribution = {};
    submissions.forEach((s) => {
      languageDistribution[s.language] =
        (languageDistribution[s.language] || 0) + 1;
    });

    return {
      totalSubmissions,
      totalAccepted: accepted,
      totalAttempted: attempted,
      acceptanceRate:
        totalSubmissions > 0 ? ((accepted / totalSubmissions) * 100).toFixed(1) : 0,
      languageDistribution,
      recentSubmissions: submissions.slice(0, 5),
    };
  } catch (error) {
    console.error("Error fetching user stats:", error);
    // Return default stats on error instead of throwing
    return {
      totalSubmissions: 0,
      totalAccepted: 0,
      totalAttempted: 0,
      acceptanceRate: 0,
      languageDistribution: {},
      recentSubmissions: [],
    };
  }
}
